package com.test.user.ticket;

public class BookDTO {

	private String seq;
	private String showroundSeq;
	private String couponUserSeq;
	private String inputDate;
	private String ticketNum;
	private int cusseq;
	
	public int getCusseq() {
		return cusseq;
	}
	public void setCusseq(int cusseq) {
		this.cusseq = cusseq;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getShowroundSeq() {
		return showroundSeq;
	}
	public void setShowroundSeq(String showroundSeq) {
		this.showroundSeq = showroundSeq;
	}
	public String getCouponUserSeq() {
		return couponUserSeq;
	}
	public void setCouponUserSeq(String couponUserSeq) {
		this.couponUserSeq = couponUserSeq;
	}
	public String getInputDate() {
		return inputDate;
	}
	public void setInputDate(String inputDate) {
		this.inputDate = inputDate;
	}
	public String getTicketNum() {
		return ticketNum;
	}
	public void setTicketNum(String ticketNum) {
		this.ticketNum = ticketNum;
	}
	
	
	
}
